/**
 * 常用的正则表达式
 */
export const RegularExpression = {
    /**
     * 手机的正则
     */
    'matchPhone': /^[1][3,4,5,7,8,9][0-9]{9}$/,

    /**
     * 身份证的正则
     */
    'matchIDCard': /^[1-9]\d{5}(18|19|([2]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$/,

    /**
     * 因为ios 中文键盘输入英文会出现这个bug
     * IOS 6分之1的正则
     */
    'matchIOSSpace': /\u2006/g,

    /**
     * 空格的正则
     */
    'matchSpace': /(^\s*)|(\s*$)/g,
}; 
